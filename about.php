<h1 class="title" page="about"> About </h1>

<p> Marquette ACM is a student chapter of the world's largest educational and scientific computing society, the
    <a href='https://www.acm.org'>Association for Computing Machinery (ACM)</a>.</p>
<p>Marquette ACM aims to connect and create a community for Marquette students of all majors interested in computing.
</p>
<p>We send out periodic notifications to all ACM members to inform them of upcoming events, and other useful
    information. If you would like to receive these emails, please <a href="/subscribe">subscribe to our email list!</a>
</p>
<p>For detailed information about how ACM is run here at Marquette, take a look at our <a
        href="/constitution.pdf">constitution</a>. If you would like to contact us, you can do so <a href="mailto:acm-officers@mscs.mu.edu">
        here</a>.
</p>

